-- | Convenience method utilising default fields to construct
-- the minimum required request.
deleteLayer :: Text
            -> DeleteLayer
deleteLayer p1 = DeleteLayer
    { dlsLayerId = p1
    }

data DeleteLayer = DeleteLayer
    { dlsLayerId :: !Text
      -- ^ The layer ID.
    } deriving (Eq, Show, Generic)

instance ToJSON DeleteLayer where
    toJSON = genericToJSON jsonOptions

instance AWSRequest DeleteLayer where
    type Er DeleteLayer = OpsWorksError
    type Rs DeleteLayer = DeleteLayerResponse
    request  = getJSON service
    response = responseJSON

data DeleteLayerResponse = DeleteLayerResponse
    deriving (Eq, Show, Generic)

instance FromJSON DeleteLayerResponse where
    fromJSON = genericFromJSON jsonOptions

